#include "libft.h"

void	ft_strclr(char *s)
{
	int		i;
	int		size;

	i = 0;
	size = 0;
	if (!s)
		return ;
	while (s[size])
		size++;
	while (i < size)
	{
		s[i] = '\0';
		i++;
	}
}
